def ra(doc, find: str, repl: str) -> None:
    rng = doc.Content
    rng.Find.ClearFormatting()
    rng.Find.Replacement.ClearFormatting()
    rng.Find.Execute(
        FindText=find,
        MatchCase=False,
        MatchWholeWord=False,
        Forward=True,
        Wrap=1,
        Format=False,
        ReplaceWith=repl,
        Replace=2,
    )


def rb(doc, start: str, end: str, body: str) -> None:
    c = doc.Content
    f = c.Duplicate
    f.Find.ClearFormatting()
    if not f.Find.Execute(start):
        raise RuntimeError(f"start not found: {start}")
    s = f.Start

    g = doc.Range(s, doc.Content.End)
    g.Find.ClearFormatting()
    if not g.Find.Execute(end):
        raise RuntimeError(f"end not found: {end}")
    e = g.Start

    t = doc.Range(s, e)
    t.Text = start + "\r" + body.strip() + "\r\r" + end


